package ex_29;

public class Lab079 {
    public static void main(String[] args) {
        // Ternary Operator
       // ? if true do this : else do that
      //  boolean result = 10 > 40 ? false : true; // 89 : 91
        //   System.out.println(result);
        int a1 = (30 > 40) ? 10:20;
        System.out.println(a1);

        String str = 10 > 20 ? "10" : "TWENTY";
        System.out.println(str);
    }
}
